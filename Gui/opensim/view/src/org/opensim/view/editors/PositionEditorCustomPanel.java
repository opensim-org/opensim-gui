    void setVec3(Vec3 d) {
        dPoint=new Vec3(d.get());
        jSpinnerX.setValue(d.get(0));
        jSpinnerY.setValue(d.get(1));
        jSpinnerZ.setValue(d.get(2));
    }

    Vec3 getVec3() {
        return new Vec3((Double)jSpinnerX.getValue(), (Double)jSpinnerY.getValue(), (Double)jSpinnerZ.getValue());
    }

